// 4.2 Operadores de comparación
// Los operadores de comparación se utilizan para comparar dos valores 
// y devolver un valor booleano (true o false).

// 4.2.1. Operador de igualdad (==)
// El operador de igualdad (==) se utiliza para comparar dos valores
// y devolver true si son iguales o false si son diferentes.

// Ejemplo:
const a = 5;
const b = 5;
const equal = a == b; // equal es true
console.log(equal); // true

// 4.2.2. Operador de desigualdad (!=)
// El operador de desigualdad (!=) se utiliza para comparar dos valores
// y devolver true si son diferentes o false si son iguales.

// Ejemplo:
const a1 = 5;
const b1 = 5;
const notEqual = a1 != b1; // notEqual es false

// 4.2.3. Operador de mayor que (>)
// El operador de mayor que (>) se utiliza para comparar dos valores
// y devolver true si el primero es mayor que el segundo o false si es menor.

// Ejemplo:
const a2 = 5;
const b2 = 3;
const greaterThan = a2 > b2; // greaterThan es true

// 4.2.4. Operador de menor que (<)
// El operador de menor que (<) se utiliza para comparar dos valores
// y devolver true si el primero es menor que el segundo o false si es mayor.

// Ejemplo:
const a3 = 5;
const b3 = 3;
const lessThan = a3 < b3; // lessThan es false

// 4.2.5. Operador de mayor o igual que (>=)
// El operador de mayor o igual que (>=) se utiliza para comparar dos valores
// y devolver true si el primero es mayor o igual que el segundo o false si es menor.

// Ejemplo:
const a4 = 5;
const b4 = 3;
const greaterThanOrEqual = a4 >= b4; // greaterThanOrEqual es true

// 4.2.6. Operador de menor o igual que (<=)
// El operador de menor o igual que (<=) se utiliza para comparar dos valores
// y devolver true si el primero es menor o igual que el segundo o false si es mayor.

// Ejemplo:
const a5 = 5;
const b5 = 3;
const lessThanOrEqual = a5 <= b5; // lessThanOrEqual es false

// 4.2.7. Operador de igualdad estricta (===)
// El operador de igualdad estricta (===) se utiliza para comparar dos valores
// y devolver true si son iguales y del mismo tipo o false si son diferentes.

// Ejemplo:
const a6 = 5;
const b6 = "5";
const strictEqual = a6 === b6; // strictEqual es false

// 4.2.8. Operador de desigualdad estricta (!==)
// El operador de desigualdad estricta (!==) se utiliza para comparar dos valores
// y devolver true si son diferentes o false si son iguales y del mismo tipo.

// Ejemplo:
const a7 = 5;
const b7 = "5";
const strictNotEqual = a7 !== b7; // strictNotEqual es true

