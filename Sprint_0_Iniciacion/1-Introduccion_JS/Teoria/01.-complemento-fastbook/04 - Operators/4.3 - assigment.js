// 4.3 Operadores de asignación
// Los operadores de asignación se utilizan para asignar un valor a una variable.

// 4.3.1. Operador de asignación (=)
// El operador de asignación (=) se utiliza para asignar un valor a una variable.

// Ejemplo:
let x = 5;
console.log(x); // 5

// 4.3.2. Operador de asignación de suma (+=)
// El operador de asignación de suma (+=) se utiliza para sumar un valor a una variable.

// Ejemplo:
let x1 = 5;
x1 += 2; // x es 7
console.log(x1); // 7

// 4.3.3. Operador de asignación de resta (-=)
// El operador de asignación de resta (-=) se utiliza para restar un valor a una variable.

// Ejemplo:
let x2 = 5;
x2 -= 2; // x es 3
console.log(x2); // 3

// 4.3.4. Operador de asignación de multiplicación (*=)
// El operador de asignación de multiplicación (*=) se utiliza para multiplicar un valor a una variable.

// Ejemplo:
let x3 = 5;
x3 *= 2; // x es 10
console.log(x3); // 10

// 4.3.5. Operador de asignación de división (/=)
// El operador de asignación de división (/=) se utiliza para dividir un valor a una variable.

// Ejemplo:
let x4 = 5;
x4 /= 2; // x es 2.5
console.log(x4); // 2.5

