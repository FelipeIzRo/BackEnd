// 04 - Operators
// Los operadores son símbolos que indican al motor de JavaScript que realice una operación aritmetica, lógica o de comparación y devuelva un resultado.

// 4.1. Operadores aritméticos
// Los operadores aritméticos son los que se utilizan para realizar operaciones matemáticas.

// 4.1.1. Operador de suma (+)
// El operador de suma (+) se utiliza para sumar dos valores.

// Ejemplo:
const num1 = 5;
const num2 = 2;
const add = num1 + num2; // c es 7
console.log(add); // 7

// 4.1.2. Operador de resta (-)
// El operador de resta (-) se utiliza para restar dos valores.

// Ejemplo:
const a = 5;
const b = 2;
const c = a - b; // c es 3
console.log(c); // 3


// 4.1.3. Operador de multiplicación (*)
// El operador de multiplicación (*) se utiliza para multiplicar dos valores.

// Ejemplo:
const multiplier1 = 5;
const multiplier2 = 2;
const product = multiplier1 * multiplier2; // c es 10
console.log(product); // 10

// 4.1.4. Operador de división (/)
// El operador de división (/) se utiliza para dividir dos valores.

// Ejemplo:
const dividend = 5;
const divisor = 2;
const quotient = dividend / divisor; // c es 2.5
console.log(quotient); // 2.5

// 4.1.5. Operador de módulo (%)
// El operador de módulo (%) se utiliza para obtener el resto de una división.

// Ejemplo:
const dividend1 = 5;
const divisor1 = 2;
const remainder = dividend1 % divisor1; // c es 1
console.log(remainder); // 1

// 4.1.6. Operador de incremento (++)
// El operador de incremento (++) se utiliza para incrementar en 1 el valor de una variable.

// Ejemplo:
let num = 5;
num++; // num es 6
console.log(num); // 6

// 4.1.7. Operador de decremento (--)
// El operador de decremento (--) se utiliza para decrementar en 1 el valor de una variable.

// Ejemplo:
let num3 = 5;
num3--; // num es 4
console.log(num3); // 4

// 4.1.8. Operador de exponenciación (**)
// El operador de exponenciación (**) se utiliza para elevar un número a una potencia.

// Ejemplo:
const base = 2;
const exponent = 3;
const result = base ** exponent; // c es 8
console.log(result); // 8
