// 02 - Variables

// Una variable es un espacio en memoria que se reserva para almacenar un valor.
// En JavaScript se declaran con la palabras reservadas
// var, const o let seguida del nombre de la variable.

// var: es la forma antigua de declarar variables, no se recomienda su uso.
// const: es la forma moderna de declarar variables, se recomienda su uso.
// let: es la forma moderna de declarar variables, se recomienda su uso.

// Ejemplos:

var personName = "Juan"; // Variable de tipo string
console.log(personName); // Juan

const PI = 3.1416; // Variable de tipo number
console.log(PI); // 3.1416

let isAdult = true; // Variable de tipo boolean
console.log(isAdult); // true